package net.ayman.model;

public class ProvidedBy {

    private int staffId;
    private int serviceId;
    private int newStaffId;
    private int newServiceId;

    public int getStaffId() {
        return staffId;
    }

    public void setStaffId(int staffId) {
        this.staffId = staffId;
    }

    public int getServiceId() {
        return serviceId;
    }

    public void setServiceId(int serviceId) {
        this.serviceId = serviceId;
    }

    public int getNewStaffId() {
        return newStaffId;
    }

    public void setNewStaffId(int newStaffId) {
        this.newStaffId = newStaffId;
    }

    public int getNewServiceId() {
        return newServiceId;
    }

    public void setNewServiceId(int newServiceId) {
        this.newServiceId = newServiceId;
    }
}
